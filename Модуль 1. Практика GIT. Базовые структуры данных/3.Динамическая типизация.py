"""
Динамическая типизация - определение типа данных переменных происходит во время выполнения программы.
Строка
"""
name = 'Urban'
print(name, type(name)) 

"""
Целое число - integer
"""
name = 5
print(name, type(name))

"""
Число с дробью - float
"""
name = 5.5
print(name, type(name))

"""
Список(у порядоченный набо элементов, каждый из которых имеет свой индекс - list)
"""
name = [1, 2]
print(name, type(name))

